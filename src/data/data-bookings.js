import { add } from 'date-fns';

function fromToday(numDays, todo = 'start') {
  const date = add(new Date(), { days: numDays });
  const newDate =
    todo === 'start'
      ? new Date(date.setUTCHours(15, 0, 0, 0)).toISOString()
      : todo === 'end'
      ? new Date(date.setUTCHours(12, 0, 0, 0)).toISOString()
      : new Date(date.setUTCHours(0, 0, 0, 0)).toISOString();
  return newDate;
}

